# ── web_search クエリテンプレート ───────────────────────────────────────────

def build_search_queries(company: str, ticker_code: str, sector: str = "") -> list[str]:
    """銘柄ごとの検索クエリリストを生成する"""
    from datetime import date
    today = date.today().strftime("%Y年%m月")
    year = date.today().year

    queries = [
        f"{company} 株 最新ニュース",
        f"{company} 決算 業績 {year}",
        f"{company} アナリスト 目標株価 {year}",
        f"NYダウ ナスダック 前日終値 {today}",
        f"ドル円 為替 {today}",
        f"日経平均 先物 {today}",
    ]
    if sector:
        queries.append(f"{sector}セクター 市場動向 {today}")

    return queries
