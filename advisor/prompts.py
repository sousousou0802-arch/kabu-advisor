def build_market_search_queries() -> list[str]:
    """
    市場全体の「ファクト」のみを1クエリで収集する（Geminiレート制限対策）。
    """
    from datetime import date
    today = date.today().strftime("%Y年%m月%d日")
    return [
        f"{today} 日経平均終値 NYダウ SP500 ナスダック終値 ドル円 日経先物 東証売買代金 決算発表予定",
    ]


