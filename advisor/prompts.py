def build_market_search_queries() -> list[str]:
    """市場全体の状況を把握するための検索クエリ"""
    from datetime import date
    today = date.today().strftime("%Y年%m月%d日")
    return [
        f"日経平均 今日 {today} 見通し",
        f"NYダウ ナスダック 前日終値 {today}",
        f"ドル円 為替 {today}",
        f"日本株 注目銘柄 今日 {today}",
        f"東証 出来高 急増 銘柄 {today}",
        f"日本株 決算発表 今週",
        f"日経平均 先物 {today}",
    ]


